/*
* Operator Binary adalah operator yang melibatkan 2 buah nilai ( operand )
* operator Aritamatika adalah operator yang digunakan untuk operasi aritmatika
*/

#include<iostream>

using namespace std;

int main()
{
	int a = 7;
	int b = 5;

	cout << "a : " << a << endl;
	cout << "b : " << b << endl << endl;

	cout << "Hasil a + b : " << a + b << endl;
	cout << "Hasil a - b : " << a - b << endl;
	cout << "Hasil a * b : " << a * b << endl;
	cout << "Hasil a / b : " << a / b << endl;
	cout << "Hasil a % b : " << a % b << endl;

	system("pause>0");
}